namespace SeaBattle.Backend.Domain.Models;

/// <summary>
/// Базовый класс для всех доменных сущностей, предоставляющий общие поля.
/// </summary>
public abstract class EntityBase
{
    /// <summary>
    /// Уникальный идентификатор сущности.
    /// Зачем нужно: Является первичным ключом для всех сущностей в базе данных.
    /// Позволяет однозначно идентифицировать каждую запись.
    /// </summary>
    public Guid Id { get; set; }

    /// <summary>
    /// Дата и время создания сущности (в формате UTC).
    /// Зачем нужно: Для аудита, отслеживания, когда запись была создана.
    /// </summary>
    public DateTime Created { get; set; } = DateTime.UtcNow;

    /// <summary>
    /// Дата и время последнего изменения сущности (в формате UTC).
    /// Зачем нужно: Для аудита, отслеживания изменений и поддержки оптимистичной блокировки (если потребуется).
    /// Всегда должно обновляться при изменении сущности.
    /// </summary>
    public DateTime Modified { get; set; } = DateTime.UtcNow;
}